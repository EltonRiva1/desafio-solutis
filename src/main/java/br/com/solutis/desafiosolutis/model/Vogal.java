package br.com.solutis.desafiosolutis.model;

public class Vogal {
	private String entradaDados, vogal, tempoTotal;

	public Vogal() {
		super();
	}

	public String getEntradaDados() {
		return entradaDados;
	}

	public void setEntradaDados(String entradaDados) {
		this.entradaDados = entradaDados;
	}

	public String getVogal() {
		return vogal;
	}

	public void setVogal(String vogal) {
		this.vogal = vogal;
	}

	public String getTempoTotal() {
		return tempoTotal;
	}

	public void setTempoTotal(String tempoTotal) {
		this.tempoTotal = tempoTotal;
	}

}
